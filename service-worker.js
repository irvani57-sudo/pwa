self.addEventListener("install", (event) => {
  console.log("? Service Worker ??? ??");

  event.waitUntil(
    caches.open("omidepay-cache-v1").then((cache) => {
      // ??? ???? ???? ?? ?? index.html ????? ????? ? cache ?? ?? ?????? ?????
      return cache.addAll([
        "./",
        "index.html", 
        "manifest.json",
        "icons/icon-192.png",
        "icons/icon-512.png"
      ]);
    })
  );
});

self.addEventListener("fetch", (event) => {
  event.respondWith(
    caches.match(event.request).then((response) => {
      // ??? ?? Cache ?? ??????? ??? ???? ???? ???? ????
      return response || fetch(event.request);
    })
  );
});

